package isep.a1.harry_potter_at_home_javafx.modele.Wizard;

import java.util.Scanner;

public enum Pet {
    Owl, Rat, Cat, Toad;

    public static Pet assignPet() {
        GameMethod myGamemethod= new GameMethod();
        Scanner scanner = new Scanner(System.in);
        System.out.println("Select your pet :");
        System.out.println("1 = Cat");
        System.out.println("2 = Owl");
        System.out.println("3 = Toad");
        System.out.println("4 = Rat");
        int choose = myGamemethod.readInt("-> ", 4);
        if (choose == 1){
            return Pet.Cat;
        }
        if (choose == 2){
            return Pet.Owl;
        }
        if (choose == 3){
            return Pet.Toad;
        }
        else {
            return Pet.Rat;
        }
    }
}